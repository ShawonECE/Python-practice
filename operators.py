
a, b = 26, 10

#arithmetic operators
print(a+b)
print(a-b)
print(a/b)
print(a%b)
print(a//b)
print(a*b)
print(a**b)

#relational operators
print(a==b)
print(a>b)
print(a<b)
print(a>=b)
print(a!=b)

#condition_logical operators
name = 'Shawon'
age = 25
name_matched = name == 'Shawon'
age_matched = age == 26

print(name_matched)
print(age_matched)
print(age_matched and name_matched)
print(age_matched or name_matched)
print(not name_matched)